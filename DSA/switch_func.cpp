/*
Switch case + Function 
*/
#include<iostream>

using namespace std;


int main()
{
    int num =2;

    cout<< endl;

    switch(num)
    {
        case 1:cout<<"1\n";
            break;// to get out of a block
        case 2:cout<<"2\n";
            break;
        default:
        cout<<"its default\n";
    }
    return 0;
}
